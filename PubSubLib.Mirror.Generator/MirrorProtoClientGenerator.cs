using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace PubSubLib.Mirror.Generator;

[Generator(LanguageNames.CSharp)]
public sealed class MirrorProtoClientGenerator : IIncrementalGenerator
{
    private const string AttrName = "PubSubLib.Mirror.MirrorProtoClientAttribute";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var classes = context.SyntaxProvider.ForAttributeWithMetadataName(
            AttrName,
            predicate: static (node, _) => node is ClassDeclarationSyntax,
            transform: static (ctx, _) => TransformClass(ctx)
        );

        var valid = classes.Where(static c => c is not null);

        context.RegisterSourceOutput(valid, static (spc, info) => GenerateCode(spc, info!.Value));
    }

    private static MirrorClassInfo? TransformClass(GeneratorAttributeSyntaxContext ctx)
    {
        var classDecl = (ClassDeclarationSyntax)ctx.TargetNode;
        var classSymbol = (INamedTypeSymbol)ctx.TargetSymbol;

        if (!classDecl.Modifiers.Any(m => m.IsKind(SyntaxKind.PartialKeyword)))
            return null;

        var attr = ctx.Attributes[0];
        if (attr.ConstructorArguments.Length < 1 || attr.ConstructorArguments[0].Value is not INamedTypeSymbol protoType)
            return null;

        var dataName = protoType.Name;

        var ns = classSymbol.ContainingNamespace?.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)
            .TrimStartGlobalPrefix() ?? "";

        var fields = new List<FieldMapping>();
        var protoProps = protoType.GetMembers().OfType<IPropertySymbol>().ToDictionary(p => p.Name);

        foreach (var member in classSymbol.GetMembers())
        {
            if (member is not IFieldSymbol fieldSymbol) continue;
            if (!fieldSymbol.Name.StartsWith("_")) continue;

            var propName = ToPascalCase(fieldSymbol.Name);
            if (!protoProps.TryGetValue(propName, out var protoProp)) continue;

            var typeName = fieldSymbol.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)
                .TrimStartGlobalPrefix();

            fields.Add(new FieldMapping(fieldSymbol.Name, propName, typeName));
        }

        var fullProtoName = protoType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)
            .TrimStartGlobalPrefix();

        return new MirrorClassInfo(ns, classSymbol.Name, fullProtoName, dataName, fields.ToArray());
    }

    private static string ToPascalCase(string fieldName)
    {
        if (fieldName.Length <= 1) return fieldName;
        return char.ToUpperInvariant(fieldName[1]) + fieldName.Substring(2);
    }

    private static void GenerateCode(SourceProductionContext context, MirrorClassInfo info)
    {
        if (info.Fields.Length == 0) return;

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS8618");
        sb.AppendLine();
        if (!string.IsNullOrEmpty(info.Namespace))
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }
        sb.AppendLine($"partial class {info.ClassName} : global::PubSubLib.Mirror.IPlayerMirrorClient");
        sb.AppendLine("{");
        sb.AppendLine($"    private {info.ProtoTypeFullName}? _mirrorProto;");
        sb.AppendLine();
        sb.AppendLine("    private long ___gs_playerId;");
        sb.AppendLine("    public long PlayerId { get => ___gs_playerId; set => ___gs_playerId = value; }");
        sb.AppendLine();
        sb.AppendLine($"    public string DataName => \"{info.DataName}\";");
        sb.AppendLine();
        sb.AppendLine($"    public {info.ProtoTypeFullName} GetMirrorProto()");
        sb.AppendLine("    {");
        sb.AppendLine("        if (_mirrorProto is null)");
        sb.AppendLine($"            _mirrorProto = new {info.ProtoTypeFullName}();");
        sb.AppendLine("        return _mirrorProto;");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    partial void OnCommit(string commit);");
        sb.AppendLine();

        foreach (var f in info.Fields)
        {
            sb.AppendLine($"    public {f.TypeName} {f.PropertyName}");
            sb.AppendLine("    {");
            sb.AppendLine($"        get => {f.FieldName};");
            sb.AppendLine($"        set => {f.FieldName} = value;");
            sb.AppendLine("    }");
            sb.AppendLine();
        }

        sb.AppendLine("    public void SyncFromProto()");
        sb.AppendLine("    {");
        sb.AppendLine("        var proto = GetMirrorProto();");
        foreach (var f in info.Fields)
            sb.AppendLine($"        {f.FieldName} = proto.{f.PropertyName};");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    public void ApplyUpdate(byte[] data, string commit)");
        sb.AppendLine("    {");
        sb.AppendLine($"        var proto = {info.ProtoTypeFullName}.Parser.ParseFrom(data);");
        sb.AppendLine("        _mirrorProto = proto;");
        sb.AppendLine("        SyncFromProto();");
        sb.AppendLine("        OnCommit(commit);");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        context.AddSource($"{info.ClassName}.g.cs", sb.ToString());
    }
}
